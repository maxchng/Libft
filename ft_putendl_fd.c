#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int		i;
	char	nl;

	i = 0;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	nl = '\n';
	write(fd, &nl, 1);
}
